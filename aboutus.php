<h2>Sejarah Sekolah</h2>
<p>Sekolah ini telah ditubuhkan pada 2 hb April 1962 dengan kelasnya menumpang di Sekolah Inggeris Derma. Dalam tahun 1963 kelas telah dipindah dan ditumpangkan di Sekolah Kangar lelaki yang terletak di tapak berhampiran Pejabat Pos Kangar hari ini.</p>
<p>Pembelajaran di sekolah ini bermula pada 7 mei 1963 ( dalam penggal kedua). Pada ketika itu jumlah pelajar ialah seramai 326 orang dengan 10 bilik darjah ( 9 aliran melayu dan 1 aliran Inggeris ). Seramai 15 orang guru mengajar pada masa itu, termasuk Guru Besar iaitu Encik Abdullah Bin Zakaria. Ketika itu, sekolah telah di beri nama Sekolah Pelajaran Lanjutan. YTM Tengku Sulaiman Ign Al Marhum Tengku Bendahara Abu Bakar telah merasmikan pembukaanya pada 20hb Ogos 1963.</p>
<p>Memandangkan bilangan pelajar yang semakin bertambah, sebuah lagi bangunan didirikan bagi menampung para pelaajr seramai 465 orang. Bangunan ini dirasmikan oleh Duli Yang Maha Mulia Tuanku Raja Perlis, Almarhum Syed Putra Ibn Almarhum Syed Hassan Jamalullail pada 14hb Mei 1968. Yang Berhormat Menteri Pelajaran, Encik Mohd Khir Johari dan YAB Menteri Besar Perlis turut hadir bersama untuk berucap dalam majlis tersebut. Bersempena itu, sekolah ini telah diubah nama kepada Sekolah Menengah Syed Sirajuddin iaitu sempena nama YTM Raja Muda Perlis masa itu. Jumlah guru pada masa itu seramai 22 orang.</p>
<p>Berikutan dengan perkembangan pelajaran sains dan teknologi sebuah lagi bangunan didirikan untuk dijadikan makmal pada tahun 1971 bagi memenuhi keperluan tersebut. Perpustakaan merupakan tempat membekalkan alat bantu yang penting dalam proses pembelajaran. Lantaran itu, pada tahun 1975 sebuah perpustakaan telah didirikan dan dirasmikan oleh YB Datuk Mansor Osman, Setiausaha Kerajaan Negeri Perlis pada 6hb Julai 1977.</p>
<p>Pada tahun 1977, sekolah ini telah di naik taraf sebagai sekolah menengah atas iaitu dengan wujudnya 2 kelas tingkatan empat . Sekitar tahun 1988 hingga 1990 sebuah bangunan bilik darjah 2 tingkat didirikan serta dua buah bangunan bilik darjah satu tingkat didirikan secara bergotong royong oleh ibu bapa dan kakitangan sekolah. Seterusnya pada tahun 1977 dua buah kelas dan sebuah makmal sains telah dibina.</p>
<p>Sekolah ini mencapai kemajuan dari segi pendidikan berikutan kewujudan sebuah kelas sains tulen yang diperkenalkan sejak tahun 1995. Dengan kemasukan alaf baru 2000, pihak sekolah telah mengambil inisiatif membina sebuah bangunan lagi bagi menampung bilangan pelajar yang semakin meningkat. Bangunan 2 tingkat tersebut terdiri daripada lima buah bilik darjah dan tiga buah bengkel Kemahiran Hidup.</p>
<p>Pada tahun 2000 sekolah ini telah mewujudkan persekolahan sesi pagi untuk tingkatan dua, tiga, empat dan lima manakala sesi petang bagi pelajar tingkatan satu. Demi mewujudkan semula sistem persekolahan satu sesi maka sebuah bangunan terdiri daripada lima bilik darjah didirikan pada tahun 2003.</p>
<p>Pada Disember 2007 dua buah bangunan gotong royong dan bangunan tingkatan enam dirobohkan untuk didirikan tiga buah bangunan empat tingkat. Pada tahun 2008 sesi pengajaran dan pembelajaran untuk kelas kelas tertentu diadakan.</p>
<p>Pelaksanaan OPs English & Pendidikan Teknik dan Vokasional (PTV)

Program Oral Proficiency in English for Secondary Schools ( Ops English)  telah diperkenalkan oleh Kementerian Pelajaran Malaysia untuk meningkatkan kemahiran mendengar dan bertutur oleh pelajar Malaysia dan telah dilaksanakan di SMK Syed Sirajuddin pada tahun 2014.  Begitu juga dengan Pendidikan Asas Vokasional (PAV) juga telah dilaksanakan pada tahun 2014. Program ini adalah untuk menyediakan peluang kepada murid lepasan UPSR agar menjadi modal insan berkemahiran bekerja dan bersedia melanjutkan pembelajaran ke peringkat yang lebih tinggi.</p>
